    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String id = jTextField4.getText();
    String nombre = jTextField3.getText();
    String documento = jTextField2.getText();
    String correo = jTextField1.getText();
    String telefono = jTextField6.getText();
    
    System.out.println("---- DATOS CAPTURADOS ----");
    System.out.println("ID Productor: [" + id + "]");
    System.out.println("Nombre Productor: [" + nombre + "]");
    System.out.println("Documento: [" + documento + "]");
    System.out.println("Correo: [" + correo + "]");
    System.out.println("TelÃ©fono: [" + telefono + "]");
    System.out.println("---------------------------");

    if (id.isEmpty() || nombre.isEmpty() || documento.isEmpty() || correo.isEmpty() || telefono.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor completa todos los campos.");
        return;
    }

    String sql = "INSERT INTO PRODUCTOR (ID_PRODUCTOR, NOMBRE_PRODUCTOR, CEDULA_CIUDADANIA, CORREO, TELEFONO) VALUES (?, ?, ?, ?, ?)";

   try (Connection conn = ConexionDB.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setInt(1, Integer.parseInt(id));
        ps.setString(2, nombre);
        ps.setString(3, documento);
        ps.setString(4, correo);
        ps.setString(5, telefono);

        ps.executeUpdate();

        JOptionPane.showMessageDialog(this, "Productor creado correctamente.");
        limpiarCampos();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al crear el productor: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

